#include "libft.h"

void		ft_striter(char *s, void (*f)(char*))
{
	size_t	i;

	if ((!s) || (!f))
		return ;
	if (!s)
		return ;
	i = ft_strlen(s);
	while (i-- > 0)
		f(s++);
}
