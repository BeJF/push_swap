#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 1 + ft_strlen(s);
	while (i != 0)
	{
		if (*s == (char)c)
			return ((char*)(s));
		i--;
		s++;
	}
	return (0);
}
